async function generateContent(
  settings: AISettings,
  topic: string,
  numSlides: number,
  isExactTitle: boolean,
  ctaType: 'custom' | 'subscribe',
  customCtaText: string,
) {
  const contentSlides = numSlides - 2;

  let ctaBlock = '';
  if (ctaType === 'custom' && customCtaText.trim()) {
    ctaBlock = `ФИНАЛЬНЫЙ СЛАЙД (CTA):
- "title": короткий эмоциональный заголовок (2-4 слова CAPS), подходящий к этому призыву
- "description": ИСПОЛЬЗУЙ ТОЧНО ЭТОТ ТЕКСТ: "${customCtaText}"`;
  } else {
    ctaBlock = `ФИНАЛЬНЫЙ СЛАЙД (CTA):
- "title": эмоциональный призыв 2-4 слова (CAPS). Примеры: "ЗАБЕРИ ЭТОТ МАТЕРИАЛ", "СОХРАНИ СЕБЕ", "НЕ ПОТЕРЯЙ"
- "description": живой текст-призыв. НЕ канцелярит. Как будто говоришь другу: "Напиши 'промты' в директ, чтобы получить гайд. Подпишись — тут только то, что меняет мышление."`;
  }

  const titleBlock = isExactTitle
    ? `ВАЖНО: "first_page_title" ДОЛЖЕН БЫТЬ ТОЧНО: "${topic}"`
    : `ЗАГОЛОВОК ПЕРВОГО СЛАЙДА (first_page_title):
- Это ПРОВОКАЦИЯ и КРЮЧОК
- Может быть в CAPS или обычным регистром
- Может быть из двух частей через \\n (крючок + подзаголовок)
- Используй принципы: ЗАПРЕТ/СЕКРЕТ, ОПАСНОСТЬ, КОНТРАСТ, КОНКРЕТИКА, ЛИЧНОСТЬ`;

  return generateCarouselSlides(settings, topic, contentSlides, ctaBlock, titleBlock);
}

