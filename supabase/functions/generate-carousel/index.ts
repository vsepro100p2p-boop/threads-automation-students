import 'jsr:@supabase/functions-js/edge-runtime.d.ts';
import { createClient } from 'npm:@supabase/supabase-js@2';
import { callAI, hasAIKey, missingKeyError, AI_SETTINGS_COLUMNS, type AISettings } from '../_shared/ai.ts';

import { buildCors } from '../_shared/cors.ts';

Deno.serve(async (req: Request) => {
  const corsHeaders = buildCors(req);
  const jsonResponse = (data: unknown, status = 200) =>
    new Response(JSON.stringify(data), {
      status,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });

  if (req.method === 'OPTIONS') {
    return new Response(null, { status: 200, headers: corsHeaders });
  }

  try {
    const supabase = createClient(
      Deno.env.get('SUPABASE_URL')!,
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    );

    const token = req.headers.get('Authorization')?.replace('Bearer ', '');
    const { data: { user } } = await supabase.auth.getUser(token!);
    if (!user) return jsonResponse({ error: 'Unauthorized' }, 401);

    const { data: aiSettings } = await supabase
      .from('ai_settings')
      .select(AI_SETTINGS_COLUMNS)
      .eq('user_id', user.id)
      .maybeSingle();

    if (!hasAIKey(aiSettings)) {
      return jsonResponse({ error: `${missingKeyError(aiSettings)}. Set it in AI settings.` }, 400);
    }

    const body = await req.json();

    if (body.action === 'generate_topic') {
      return jsonResponse({ topic: await generateTopic(aiSettings, body.activity || '') });
    }

    if (body.action === 'generate_content') {
      return jsonResponse(
        await generateContent(
          aiSettings,
          body.topic,
          body.numSlides || 5,
          body.isCustomTitle || false,
          body.ctaType || 'subscribe',
          body.customCtaText || '',
        )
      );
    }

    return jsonResponse({ error: 'Invalid action' }, 400);
  } catch (error: any) {
    console.error('Error:', error);
    return jsonResponse({ error: error.message }, 500);
  }
});

